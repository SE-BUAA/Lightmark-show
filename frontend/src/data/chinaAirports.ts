// 前端搜索按省或直辖市选择，但传给后端的是该省全部机场码，后端复用既有 departureCity/arrivalCity 参数做匹配。
export const chinaProvinceAirportOptions: ChinaAirportOption[] = Object.values(
  chinaAirports.reduce<Record<string, ChinaAirportOption[]>>((groups, item) => {
    if (item.active === false) {
      return groups;
    }
    if (!groups[item.province]) {
      groups[item.province] = [];
    }
    groups[item.province].push(item);
    return groups;
  }, {})
)
  .map((airports) => {
    const province = airports[0].province;
    const codes = Array.from(new Set(airports.flatMap((item) => [item.code, item.cityCode]).filter(Boolean))).sort();
    const aliases = Array.from(new Set(airports.flatMap((item) => [item.name, item.airport, item.code, item.cityCode, ...(item.aliases || [])])));
    const region: ChinaAirportOption["region"] = airports.some((item) => item.region === "domestic") ? "domestic" : "international";
    return {
      code: codes.join(","),
      cityCode: codes.join(","),
      name: province,
      airport: `${airports.length} 个机场`,
      province,
      letter: provinceGroups[province] || "华东",
      region,
      active: true,
      hot: hotProvinces.has(province),
      aliases,
    };
  })
  .sort((left, right) => chinaProvinceTabs.indexOf(left.letter) - chinaProvinceTabs.indexOf(right.letter) || left.name.localeCompare(right.name, "zh-CN"));
